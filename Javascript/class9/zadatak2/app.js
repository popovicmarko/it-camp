
function Parnost(br1) {
    if (br1 % 2 === 0) {
        console.log("Funkcija je parna")
    } else {
        console.log("funkcijan nije parna")
    }
}

function Negativnost(br2) {
    if (br2 < 0) {
        console.log("Broj je nagativan")
    } else {
        console.log("Broj je pozitivan")
    }
}

Parnost(2)
Negativnost(-2)